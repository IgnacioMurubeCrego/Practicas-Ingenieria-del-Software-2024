package com.example.software;

public class Usuario {

    public String nombre;
    public String apellidos;
    public String birth;
    public String gender;
    public String email;
    public String password;

    public Usuario(String nombre, String apellidos, String birth, String gender, String email, String password) {
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.birth = birth;
        this.gender = gender;
        this.email = email;
        this.password = password;
    }
}



